    <div class="question">Can I equip items or select talents?</div>
    <div class="answer">
      No. Items and talents often come in the form of Super and Gaben upgrades
      for your abilities. For example, Walrus Punch grants Silver Edge at
      Super and 20% chance to proc at Gaben (similar to level 25 talent).
      Gaining an item includes both the stats they provide AND their
      passive/active effects.
    </div>
    <div class="question">What does upgrading my shop do?</div>
    <div class="answer">
      Upgrading your shop gives you more spell slots in the shop. A level 1 shop
      offers 4 spells, while a level 2 shop offers 6. At level 3, instead of
      gaining more spell slots, you begin to see ultimate abilities in the shop.
      Levels 4 and 5 only increase your spell slots. Some God abilities are
      affected by your shop level. The cost to upgrade your shop decreases by 1
      each round.
    </div>
