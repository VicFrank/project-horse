    <h2>General</h2>
    <div class="question">What’s with the letters next to the heroes?</div>
    <div class="answer">
      The letters represent the hero’s rank such that (S>A>B>C>D), where S is
      the strongest and D is the weakest. This is determined by things such as
      melee/range, stat gain, base stats, and attack range.
    </div>
    <div class="question">
      Is an S tier hero always better than lower tier heroes?
    </div>
    <div class="answer">
      No! You wouldn’t want Primal Beast (S tier) being your spellcaster, but
      maybe Tinker or Zeus (D tier) instead.
    </div>
    <div class="question">How do I get more heroes?</div>
    <div class="answer">
      Every player will get a new hero body at rounds 1, 3, 6, 9, 12. These are
      guaranteed to be a certain rank: D at 1, C at 3, B at 6, A at 9, and S at
      12. When selecting a body, you can click ALT to compare stats with all 3
      bodies at once.
    </div>
    <div class="question">What does rerolling my hero body do?</div>
    <div class="answer">
      Rerolling your hero body costs 2 gold and gives you a choice of 3 random
      heroes to replace it with. You will always be offered one strength, one
      agility, and one intelligence hero. They can be from any tier, meaning you
      can even get an S tier body early in the game.
    </div>
    <div class="answer">
      Rerolling your hero is important, but should be secondary to managing your
      spells. When rerolling your hero, they will retain their equipped spells
      at no cost. If you do not select a hero, you will be given a random one of
      the 3 choices. If the preparation phase ends while rerolling a hero, it
      will give you a random choice. Be careful not to reroll your hero when the
      fighting phase is merely a second away.
    </div>
