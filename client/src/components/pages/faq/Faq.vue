    <div class="answer">
      The spell deck shows you what spells may show up in the shop. Every player
      selects spells from this same pool, and the number on the top right corner
      of each spell tells you how many “copies” of the spell remain in the pool.
    </div>
    <div class="answer">
      A spell with more copies left will naturally be more frequently offered in
      the shop, so you can use this to know which spells you can try to get to
      Gaben. When a player is eliminated, all of their spells are returned to
      the pool.
    </div>
