    <div class="question">How do I upgrade spells?</div>
    <div class="answer">
      By buying duplicate copies of the same spell. Getting a spell to 1 or 2
      points strengthens the spell as it would in DOTA. A 3 point spell in
      Ability Arena is equivalent to a max level spell in DOTA. Points 4, 5, 7,
      8 do not affect the spell in any way.
    </div>
