async function getMMRFromUsernames() {
  try {
    for (const username of usernames) {
      const players = await Players.getPlayersWithName(username);
      const mmrs = players.map((row) => row.mmr);
      const mmrString = mmrs.length > 0 ? mmrs.join(", ") : "null";
      console.log(`${username}, ${mmrString}`);
    }
  } catch (error) {
    throw error;
  }
}

